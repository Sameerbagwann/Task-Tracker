#  1. Identify the type of numbers :- a. 0X7AE, b. 3+4j ,C. -01234 , d. 3.14e-2?

# Character
char = 'A'
print("Character: %c" % char)

# Signed decimal integer
integer = 42
print("Signed decimal integer: %d" % integer)

# Octal integer
octal = 42
print("Octal integer: %o" % octal)

# Hexadecimal integer (UPPERcase letters)
hexadecimal = 42
print("Hexadecimal integer: %X" % hexadecimal)

# Floating point real number
float_num = 3.14
print("Floating point real number: %f" % float_num)

# Exponential notation (with lowercase 'e')
exponential = 3.14
print("Exponential notation: %e" % exponential)
